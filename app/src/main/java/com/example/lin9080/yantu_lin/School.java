package com.example.lin9080.yantu_lin;

public class School {
    private String name;
    private int id;
    private String schoolContent;
    private String majorContent;
    private int scoreLine1;
    private int scoreLine2;
    private int scoreLine3;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSchoolContent() {
        return schoolContent;
    }

    public void setSchoolContent(String schoolContent) {
        this.schoolContent = schoolContent;
    }

    public String getMajorContent() {
        return majorContent;
    }

    public void setMajorContent(String majorContent) {
        this.majorContent = majorContent;
    }

    public int getScoreLine1() {
        return scoreLine1;
    }

    public void setScoreLine1(int scoreLine1) {
        this.scoreLine1 = scoreLine1;
    }

    public int getScoreLine2() {
        return scoreLine2;
    }

    public void setScoreLine2(int scoreLine2) {
        this.scoreLine2 = scoreLine2;
    }

    public int getScoreLine3() {
        return scoreLine3;
    }

    public void setScoreLine3(int scoreLine3) {
        this.scoreLine3 = scoreLine3;
    }
}
